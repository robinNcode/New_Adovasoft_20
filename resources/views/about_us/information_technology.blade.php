                  	In 1983, the Ministry of Science and Technology established a National Computer Committee to create the required policies. The committee was also responsible to carry out programs to expand and promote the efficacious use of the sector. In 1988, the committee was replaced by the National Computer Board. In 1990, the ministry reformed the board and reconstituted it as the Bangladesh Computer Council to monitor computer- and IT-related works in the country.<br />

                  	In 2013, it was announced that a seven-year-old Bangladeshi boy, Wasik Farhan Roopkotha, would be listed as the youngest IT expert in the Guinness Book of World Records.
                  <br />
                  <br />
                  IT industry<br />
                  <br />
                  The IT industry is a relatively new sector in the country's economy. Though it is yet to make tangible contributions in the national economy, it is an important growth industry. The Bangladesh Association of Software and Information Services (BASIS) was established in 1997 as the national trade body for software and IT service industry. Starting with only 17 member companies, by 2009 membership had grown to 326. In a study among Asian countries by Japan International Cooperation Agency in 2007–08, Bangladesh was ranked first in software and IT services competitiveness and third in competencies, after India and China.The World Bank, in a study conducted in 2008, projected triple digit growth for Bangladesh in IT services and software exports.Bangladesh was also listed as one of the top 30 Countries for Offshore Services in 2010–2011 by Gartner. The Internet penetration has also grown to 21.27 percent in 2012, up from 3.2 percent three years prior. As the Internet usage increases, the government expects the IT sector to add 7.28 percent to GDP growth by 2021.

