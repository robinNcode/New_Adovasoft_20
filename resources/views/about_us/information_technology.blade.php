                  History<br />
                  <br />
                  The first computer in Bangladesh (then East Pakistan) was an IBM mainframe 1620 series, installed in 1964 at the Dhaka center of the Pakistan Atomic Energy Commission (later the Bangladesh Atomic Energy Commission). Computer use increased in the following years, especially after the independence of Bangladesh in 1971; more-advanced IT equipment began to be set up in different educational, research and financial institutions.[3] In 1979, a computer centre, later renamed Department of Computer Science & Engineering, was established at Bangladesh University of Engineering and Technology (BUET); the centre has been playing a pivotal role in Bangladeshi IT education since its inception. Through the introduction of personal computers, the use of computers witnessed a rapid increase in the late 1980s. In 1985, succeeding several individual initiatives, the first Bengali script in computers was invented, paving the way for more intense computer activities. In 1995, use of the Internet began and locally made software started to be exported.<br />
                  
